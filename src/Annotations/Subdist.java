package Annotations;

public class Subdist {
	
	String subdistName;

	
	@Override
	public String toString() {
		return "Subdist [subdistName=" + subdistName + "]";
	}


	public String getSubdistName() {
		return subdistName;
	}

	public void setSubdistName(String subdistName) {
		this.subdistName = subdistName;
	}
}
